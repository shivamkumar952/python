#Mathematical function (+, -, *, /)
n_one=int(input("Enter The Value Of N_1 : "))
n_two=int(input("Enter The Value Of N_2 : "))

Add=n_one + n_two
print("Addition is : ",Add)

Sub=n_one - n_two
print("Subtraction is : ",Sub)

Mul=n_one * n_two
print("Multiply is : ",Mul)

Div=n_one / n_two
print("Division is : ",Div)
